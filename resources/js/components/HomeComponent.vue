<script>
  export default {
      data() {
        return {
          posts: []
        }
      },
      created() {
      let uri = 'http://vuelaravelcrud.master/api/posts';
      this.axios.get(uri).then(response => {
        this.posts = response.data.data;
      });
    },
  }
